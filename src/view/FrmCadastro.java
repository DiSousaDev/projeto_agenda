    private void txtConsultaBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtConsultaBuscarKeyPressed

        String nome = "%" + txtConsultaBuscar.getText() + "%";

        ContatoDao contatoDaoJDBC = DaoFactory.createContatoDao();
        List<Contato> contatos = contatoDaoJDBC.findByName(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaContatos.getModel();

        dados.setNumRows(0);

        contatos.stream().forEach((c) -> {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getSexo(),
                c.getLogradouro(),
                c.getNome_rua(),
                c.getNumero(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getCep(),
                c.getTel_residencial(),
                c.getTel_comercial(),
                c.getObservacoes()
            });
        });

    }//GEN-LAST:event_txtConsultaBuscarKeyPressed

