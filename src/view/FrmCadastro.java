    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {

            Contato obj = new Contato();

            obj.setNome(txtNome.getText());
            obj.setEmail(txtEmail.getText());
            obj.setSexo(Sexo.valueOf(comboSexo.getSelectedItem().toString()));
            obj.setObservacoes(txtObs.getText());
            obj.setLogradouro(txtLogradouro.getText());
            obj.setNome_rua(txtNomeRua.getText());
            obj.setNumero(txtNumero.getText());
            obj.setBairro(txtBairro.getText());
            obj.setCidade(txtCidade.getText());
            obj.setEstado(comboEstado.getSelectedItem().toString());
            obj.setCep(txtCep.getText());
            obj.setTel_residencial(txtTel_residencial.getText());
            obj.setTel_comercial(txtTel_comercial.getText());

            ContatoDao contatoDaoJDBC = DaoFactory.createContatoDao();

            if (obj.getNome().length() == 0) {
                lblObrigatorioNome.setText("*Campo Obrigatório");
            } else {
                int input = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja incluir um novo registro?", "Atenção", JOptionPane.OK_CANCEL_OPTION);
                if (input == 0) {
                    contatoDaoJDBC.insert(obj);
                    JOptionPane.showMessageDialog(null, "Registro incluído com sucesso.");
                }
            }
        } catch (HeadlessException erro) {
            JOptionPane.showMessageDialog(null, "Nenhum dado para salvar.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

