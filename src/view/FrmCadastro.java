//Metodo listar na tabela
    public void listar() {
        ContatoDao contatoDaoJDBC = DaoFactory.createContatoDao();
        List<Contato> contatos = contatoDaoJDBC.findAll();
        DefaultTableModel dados = (DefaultTableModel) tabelaContatos.getModel();

        dados.setNumRows(0);

        contatos.stream().forEach((c) -> {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getSexo(),
                c.getLogradouro(),
                c.getNome_rua(),
                c.getNumero(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getCep(),
                c.getTel_residencial(),
                c.getTel_comercial(),
                c.getObservacoes()
            });
        });

    }
